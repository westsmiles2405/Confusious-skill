export function getJudgment(issues: string[]): string {
    const intro = pick(JUDGMENT_PATTERNS);
    if (issues.length === 0) {
        return `${intro}\n\n✅ 代码通过正名审查。名正言顺，事成矣。继续保持。`;
    }
    const list = issues.map((i) => `⚖️ ${i}`).join('\n');
    return `${intro}\n\n${list}\n\n📜 以上便是名不正之处。逐一正名，方可言顺事成。`;
}

