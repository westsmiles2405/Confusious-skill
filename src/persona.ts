export function getTimeBasedGreeting(): string {
    const hour = new Date().getHours();
    if (hour < 6) {
        return '夜深矣。"寝不言"——此刻当休息，莫要熬煎身体。';
    }
    if (hour < 9) {
        return '晨光熹微。一日之计在于晨，趁此清朗，理清今日之学。';
    }
    if (hour < 12) {
        return '上午好。头脑最为清明之时，当行最要紧之事。';
    }
    if (hour < 14) {
        return '午间矣。"食不语"——先安静用膳，养足精神再战。';
    }
    if (hour < 18) {
        return '午后时光如白驹过隙。把握节奏，莫使光阴虚度。';
    }
    if (hour < 22) {
        return '日暮将至。今日之学如何？若尚有余力，可再精进一二。';
    }
    return '夜已深矣。若有未竟之事，尽力收尾；若倦了，便去歇息。';
}
